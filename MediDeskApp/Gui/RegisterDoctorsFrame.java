    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
            if (!ValidateInput()) {
                    return;
            }
            
            DoctorPojo dp = new DoctorPojo();
            dp.setDocId(docId);
            dp.setContactNo(conNo);
            dp.setDocName(selEmpName);
            dp.setEmailId(emailId);
            dp.setGender(gender);
            dp.setQualification(qual);
            dp.setSpecialist(spec);
            
            User user = new User();
            user.setLoginId(loginId);
            user.setPassword(pw1);
            user.setUserName(selEmpName);
            user.setUserType("Doctor");
            
            try {
                    boolean result1 = DoctorsDao.registerNewDoctor(dp);
                    boolean result2 = UserDao.addUser(user);
                    
                    if ( result1 && result2) {
                              JOptionPane.showMessageDialog(null, "Doctor Registered Sucessfully !", "success!", JOptionPane.INFORMATION_MESSAGE);
                              clearAll();
                              loadNextDocId();
                              return;
                    }
                   JOptionPane.showMessageDialog(null, "Doctor not registered !", "Error!", JOptionPane.ERROR_MESSAGE); 
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Doctor Detail not inserted in DB! " + e, "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
            }
    }//GEN-LAST:event_btnRegisterActionPerformed

