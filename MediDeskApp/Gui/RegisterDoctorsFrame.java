    private boolean validateConNo() {
            if(conNo.isEmpty() || conNo.length() != 10)
                return false;
            char[] mob = conNo.toCharArray();
            for(char c : mob) {
                    if(!Character.isDigit(c))
                        return false;
            }
            
            return true;
    }

