    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
            if(cbRecpId.getItemCount() == 0) {
                    JOptionPane.showMessageDialog(null, "No Recptionist is Found!");
                    return;
            }
            String recpId = cbRecpId.getSelectedItem().toString();
            
            try {
                    boolean result = ReceptionistDao.deleteByReceptionistId(recpId);
                    if(result) {
                              JOptionPane.showMessageDialog(null, "Deleted Receptionist Successfully!");
                              cbRecpId.removeItem(recpId);
                              return;
                    }
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Couldn't delete Receptionist! "+e);
                    e.printStackTrace();
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

