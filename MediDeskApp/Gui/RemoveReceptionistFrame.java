    private void loadRecpID() {
            try {
                    List<String> recpIdList = ReceptionistDao.getAllReceptionistID();
                    if(recpIdList.isEmpty()) {
                              JOptionPane.showMessageDialog(null, "No records in DB!");
                              return;
                    }
                    
                    for(String id : recpIdList) {
                              cbRecpId.addItem(id);
                    }
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Couldn't fetch Receptionist Id's!"+e);
                    e.printStackTrace();
            }
    }
