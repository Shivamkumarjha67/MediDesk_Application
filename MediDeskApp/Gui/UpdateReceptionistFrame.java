    private void loadReceptionistDetail() {
            try {
                    recpDtl = ReceptionistDao.getAllReceptionistDetail();
                    if(recpDtl.isEmpty()) {
                              JOptionPane.showMessageDialog(null, "No record found in receptionist!", "Error!", JOptionPane.ERROR_MESSAGE);
                              return;
                    }
                    
                    Set<String> recpId = recpDtl.keySet();
                    cbRecpId.removeAllItems();
                    for(String id : recpId) {
                              cbRecpId.addItem(id);
                    }
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Problem in DB!");
                    return;
            }
    }

    private boolean validateInputs() {
            recpName = txtRecName.getText().trim();
            loginId = txtRecpLogin.getText().trim();
            char[] pw1 = txtPw1.getPassword();
            char[] pw2 = txtPw2.getPassword();
            recpId = cbRecpId.getSelectedItem().toString();
            
            if(recpName.isEmpty() || loginId.isEmpty() || pw1.length == 0 || pw2.length == 0)
                return true;
            
            pwd1 = new String(pw1);
            pwd2 = new String(pw2);
            
            return false;
    }

    private boolean validatePassword() {
            if(pwd1.length() < 3 || pwd2.length() < 3) 
                return true;
            if(!pwd1.equals(pwd2))
                return true;
            
            return false;
    }

    private void clearAll() {
            txtRecpLogin.setText("");
            txtPw1.setText("");
            txtPw2.setText("");
    }
