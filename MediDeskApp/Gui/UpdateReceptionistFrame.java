    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

            if(validateInputs()) {
                    JOptionPane.showMessageDialog(null, "Please input all feild first!");
                    return;
            }
            
            if(validatePassword()) {
                    JOptionPane.showMessageDialog(null, "Please check String, it should be greater than 3");
                    return;
            }
            
            ReceptionistPojo rp = new ReceptionistPojo();
            rp.setRecpName(recpName);
            rp.setRecpGender(cbGender.getSelectedItem().toString());
            rp.setRecpId(recpId);
            
            User up = new User();
            up.setLoginId(loginId);
            up.setPassword(Encryption.encrypt(pwd1));
            System.out.println(Encryption.encrypt(pwd1));
            up.setUserName(recpName);
            up.setUserType("Receptionist");
            
            try {
                    boolean result1 = ReceptionistDao.updateReceptionist(rp , up);
                    
                    if(result1) {
                              JOptionPane.showMessageDialog(null, "Successfully updated !");
                              clearAll();
                              loadReceptionistDetail();
                              return;
                    }
                    
                    JOptionPane.showMessageDialog(null, "Could not update in DB!");
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Problem in DB!" + e);
                    e.printStackTrace();
            }
    }//GEN-LAST:event_btnUpdateActionPerformed

