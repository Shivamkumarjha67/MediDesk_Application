    private void loadReceptionistDetail() {
            try {
                    unregRecp = EmpDao.getUnregisteredReceptionist();
                    Set<String> unregRecpSet = unregRecp.keySet();
                    
                    for(String id : unregRecpSet) {
                            cbEmpId.addItem(id);
                    }
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Unable to load Unregistered Receptionist!", "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
            }
    }

    private void loadNewRecpId() {
            try {
                    String recpId = ReceptionistDao.getNextRecpId();
                    txtRecId.setText(recpId);
                    if(cbEmpId.getItemCount() == 0) {
                              JOptionPane.showMessageDialog(null, "All Receptionist Added !");
                              ManageReceptionistFrame mrf = new ManageReceptionistFrame();
                              mrf.setVisible(true);
                              this.dispose();
                              return;
                    }
                    empId = cbEmpId.getSelectedItem().toString();
                    empName = unregRecp.get(cbEmpId.getSelectedItem().toString());
                    txtRecName.setText(empName);
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Unable to load new Recptionist Id !", "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
            }
    }

    private boolean validateInput() {
            recpId = txtRecId.getText();
            recpName = txtRecName.getText();
            loginId = txtLoginId.getText().trim();
            char[] pw1 = txtPw1.getPassword();
            char[] pw2 = txtPw2.getPassword();
            pwd1 = new String(pw1);
            pwd2 = new String(pw2);
            
            if(recpId.isEmpty() || recpName.isEmpty() || loginId.isEmpty() || pw1.length == 0 || pw2.length == 0) 
                return true;
            
            return false;
    }

    private boolean ValidatePassWord() {
                if(pwd1.length() < 3 || pwd2.length() < 3)
                    return true;
                if(!pwd1.equals(pwd2))
                    return true;
                
                return false;
    }

    private void clearAll() {
            cbEmpId.removeItem(empId);
            unregRecp.remove(empId);
            txtLoginId.setText("");
            txtPw1.setText("");
            txtPw2.setText("");
    }
