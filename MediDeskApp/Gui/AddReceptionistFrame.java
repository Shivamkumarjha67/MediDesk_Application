    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
            if(validateInput()) {
                    JOptionPane.showMessageDialog(null, "Please input all the field first !");
                    return;
            }
            if(ValidatePassWord()) {
                    JOptionPane.showMessageDialog(null, "Please Match the password or should be greater than 3");
                    return;
            }
            
            try {
                    ReceptionistPojo rp = new ReceptionistPojo();
                    rp.setRecpId(recpId);
                    rp.setRecpName(recpName);
                    rp.setRecpGender(cbGender.getSelectedItem().toString());
                    
                    User up = new User();
                    up.setLoginId(loginId);
                    up.setPassword(Encryption.encrypt(pwd1));
                    up.setUserName(recpName);
                    up.setUserType("Receptionist");
                    
                    boolean result1 = UserDao.addUser(up);
                    boolean result2 = ReceptionistDao.registerNewReceptionist(rp);
                    
                    if(result1 && result2) {
                              JOptionPane.showMessageDialog(null, "Successfully added to user and receptionist !");
                              clearAll();
                              loadNewRecpId();
                              return;
                    }
                    JOptionPane.showMessageDialog(null, "Unable to add either in User and Receptionist");
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Problem in Db!" + e);
                    e.printStackTrace();
            }
    }//GEN-LAST:event_btnRegisterActionPerformed

