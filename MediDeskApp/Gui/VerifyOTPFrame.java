    private void btnVerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifyActionPerformed
            otpEntered = txtOTP.getText().trim();
            
            try {
                    int otp = Integer.parseInt(otpEntered);
                    if(otp != pp.getOtp()) {
                              JOptionPane.showMessageDialog(null, "OTP doesn't match ! Try Again.");
                              return;
                    }
                    
                    AppointmentPojo ap = new AppointmentPojo();
                    ap.setMobNo(pp.getM_no());
                    ap.setOpd(pp.getOpd());
                    ap.setPatName(txtPName.getText().trim());
                    ap.setPatientId(pp.getPatId());
                    ap.setStatus("REQUEST");
                    ap.setDate(pp.getDate().toString());
                    
                    String docName = DoctorsDao.getDocName(pp.getDocId());
                    ap.setDocName(docName);
                    
                    boolean result1 = PatientsDao.registerNewReceptionist(pp);
                    boolean result2 = AppointmentDao.registerAppointment(ap);
                    
                    if(result1 && result2) {
                              JOptionPane.showMessageDialog(null, "Patient Added to DB!" );
                              ViewPatientsFrame vpf = new ViewPatientsFrame();
                              this.dispose();
                              vpf.setVisible(true);
                              return;
                    }
                    
                    JOptionPane.showMessageDialog(null, "Couldn't add to Patients DB ! ");
            }
            catch(NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "OTP can not be in other than Number Format ! " + e);
                    e.printStackTrace();
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error in DB ! " + e);
                    e.printStackTrace();
            }
    }//GEN-LAST:event_btnVerifyActionPerformed

