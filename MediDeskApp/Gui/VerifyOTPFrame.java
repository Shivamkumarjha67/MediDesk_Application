    private void loadPatientDetails() {
            txtPName.setText(pp.getFirstName()+ " " + pp.getLastName());
            txtPhNo.setText(pp.getM_no());
    }

    private boolean validateContact() {
            char[] mbNo = txtPhNo.getText().trim().toCharArray();
            
            for(char c : mbNo) {
                    if(!Character.isDigit(c)) {
                              JOptionPane.showMessageDialog(null, "Please input phone number only in digit format");
                              return true;
                    }
            }
            
            if(mbNo.length != 10)  {
                    JOptionPane.showMessageDialog(null, "Length of mobile number of 10 is mandatory!");
                    return true;
            }
            
            return false;
    }
