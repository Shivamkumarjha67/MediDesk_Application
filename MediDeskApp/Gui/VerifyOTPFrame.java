    private void btnOTPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOTPActionPerformed
            String pNo = txtPhNo.getText().trim();
            if(validateContact()) {
                    return;
            }
            
            pp.setM_no(pNo);
            String otp = String.valueOf(pp.getOtp());
            
            try {
                    
                    Sender sender = new OTPSender();
                    boolean result = sender.send(pNo, otp);
                    
                    if(result) {
                              JOptionPane.showMessageDialog(null, "OTP sent successfully!");
                              return;
                    }
                    JOptionPane.showMessageDialog(null, "Cannot send the OTP. Please check the number !!");
            }
            catch(Exception e) {
                    JOptionPane.showMessageDialog(null, "Server Issue");
                    e.printStackTrace();
            }
    }//GEN-LAST:event_btnOTPActionPerformed

