    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
            if(cbDocId.getSelectedItem() == null) {
                    JOptionPane.showMessageDialog(null, "Please Select any Doctor Id first!", "Error!", JOptionPane.ERROR_MESSAGE);
                    return;
            }
            String docId = cbDocId.getSelectedItem().toString();
            
            try {
                    boolean result = DoctorsDao.removeDoctorByDocId(docId);
                    if(result) {
                              JOptionPane.showMessageDialog(null, "Successfully Deleted ");
                              cbDocId.removeAllItems();
                              loadAllDocId();
                              return;
                    }
                    JOptionPane.showMessageDialog(null, "Could not delete. Try Again!");
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Could not delete the record from Doctors !" + e, "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

