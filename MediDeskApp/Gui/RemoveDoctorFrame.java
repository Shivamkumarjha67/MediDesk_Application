    private void loadAllDocId() {
            try {
                    List<String> docId = DoctorsDao.getAllDocId();
                    if(!docId.isEmpty())
                    for(String id : docId) {
                              cbDocId.addItem(id);
                    }
                    else {
                              JOptionPane.showMessageDialog(null, "All doctors Removed Sucessfully !");
                              ManageDoctorsFrame mdf = new ManageDoctorsFrame();
                              mdf.setVisible(true);
                              this.dispose();
                    }
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Problem in loading Doctors Id !" + e, "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
            }
    }
