    private void loadDocId() {
            try {
                    List<String> docId = DoctorsDao.getAllDocId();
                    
                    if(docId.isEmpty()) {
                        JOptionPane.showMessageDialog(null, "There is no Doctors in hospital !!");
                        ReceptionistOptionFrame rof = new ReceptionistOptionFrame();
                        this.dispose();
                        rof.setVisible(true);
                        return;
                    }
                    
                    for(String id : docId) {
                              cbDocId.addItem(id);
                    }
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error in DB! " + e);
                    e.printStackTrace();
            }
    }

    private void loadNewPatientId() {    
            try {
                    String newPatId = PatientsDao.getNewPatientId();
                    txtPatId.setText(newPatId);
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error in DB! " + e);
                    e.printStackTrace();
            }
    }

    private void loadDate() {
            try { 
                    Date today = new Date();
                    SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yyyy");
                    String dateStr = sdf.format(today);
                    txtDate.setText(dateStr);

                    java.util.Date d = sdf.parse(dateStr);
                    long ms = d.getTime();
                    dt = new java.sql.Date(ms);
            }
            catch(ParseException e) {
                    JOptionPane.showMessageDialog(null, "Problem in Date " + e);
                    e.printStackTrace();
            }
    }

    private boolean validateInputs() {
            pp = new PatientsPojo();
            
            pp.setDocId(cbDocId.getSelectedItem().toString());
            pp.setOpd(txtOpd.getText().trim());
            pp.setPatId(txtPatId.getText());
            pp.setCity(txtCity.getText().trim());
            pp.setAddress(txtAddress.getText().trim());
            pp.setFirstName(txtFName.getText().trim());
            pp.setLastName(txtLName.getText().trim());
            pp.setGender(cbGender.getSelectedItem().toString());
            pp.setM_status(cbMStatus.getSelectedItem().toString());
            pp.setM_no(txtPhoneNo.getText().trim());
            pp.setOtp(OTP);
            pp.setDate(dt);
            pp.setStatus("REQUEST");
            
            try {
                    int age = Integer.parseInt(txtAge.getText());
                    pp.setAge(age);
            }
            catch(NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Enter Age only in number format !");
                    return true;
            }
            
            if(isContactValid()) {
                    JOptionPane.showMessageDialog(null, "Please Input Valid Mobile Number !");
                    return true;
            }
            
            if(pp.getCity().isEmpty()||pp.getFirstName().isEmpty()||pp.getLastName().isEmpty()||pp.getAddress().isEmpty() || pp.getOpd().isEmpty())
            {
                    JOptionPane.showMessageDialog(null,"Please fill all the fields");
                    return true; 
            }  
            
            return false;
    }

    private boolean isContactValid() {
            char[] mbNo = txtPhoneNo.getText().trim().toCharArray();
            
            for(char c : mbNo) {
                    if(!Character.isDigit(c))
                        return true;
            }
            
            if(mbNo.length != 10)
                return true;
            
            return false;
    }
