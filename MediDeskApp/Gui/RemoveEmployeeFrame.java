    private void loadEmpId() {
            try {
                    List<String> list = EmpDao.getAllEmployeesId();
                    
                    if(list.isEmpty()) {
                              JOptionPane.showMessageDialog(null, "All Employees deleted !", "Sucess!", JOptionPane.INFORMATION_MESSAGE);
                              ManageEmployeeFrame mef = new ManageEmployeeFrame();
                              mef.setVisible(true);
                              this.dispose();
                    }
                    
                    for(String id : list) {
                              cbEmpId.addItem(id);
                    }
;            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Problem in DB. Try Again!", "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
            }
    }
