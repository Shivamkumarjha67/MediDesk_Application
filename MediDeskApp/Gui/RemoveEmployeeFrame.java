    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
            String empId = cbEmpId.getSelectedItem().toString();
            try {
                    boolean result = EmpDao.removeEmployee(empId);
                    if(result) {
                              JOptionPane.showMessageDialog(null, "Sucessfully Deleted!", "sucess!", JOptionPane.INFORMATION_MESSAGE);
                              cbEmpId.removeAllItems();
                              loadEmpId();
                              return;
                    }
                    JOptionPane.showMessageDialog(null, "Not Deleted!", "Error!", JOptionPane.ERROR_MESSAGE);

                    
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error in DB " + e, "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

