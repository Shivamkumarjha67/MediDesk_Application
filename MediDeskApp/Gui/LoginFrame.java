    private boolean validateInputs() {
            getInputs();
            if(password.length == 0 || LoginId.isEmpty())
                return true;
            
            pwd = new String(password);
            
            return false;
    }

    private void getInputs() {
            password = txtPassword.getPassword();
            LoginId = txtLoginId.getText();
            userType = getUserType();
    }

    private String getUserType() {
            if(rbDoctor.isSelected())
                return rbDoctor.getText();
            else if(rbReceptionist.isSelected())
                return rbReceptionist.getText();
            else if(rbAdmin.isSelected())
                return rbAdmin.getText();
            else
                return null;
    }
