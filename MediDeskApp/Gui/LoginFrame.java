    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed

            boolean validInputs = validateInputs();
            
            if(validInputs) {
                    JOptionPane.showMessageDialog(null, "Please Fill all the feild first", "Error!", JOptionPane.ERROR_MESSAGE);
                    return;
            }
            
            User user = new User();
            user.setLoginId(LoginId);
            user.setPassword(Encryption.encrypt(pwd));
            user.setUserType(userType);
            
            try {
                    String name = UserDao.validateUser(user);
                    System.out.println(name);
             
                    if(name != null) {
                              UserProfile.setUserId(LoginId);
                              UserProfile.setUserName(name);
                              UserProfile.setUserType(userType);
                              
                              JFrame optionFrame = null;
                              
                              if(userType.equalsIgnoreCase("admin"))
                                        optionFrame = new AdminOptionFrame();
                              else if(userType.equalsIgnoreCase("doctor"))
                                        optionFrame = new DoctorOptionFrame();
                              else
                                        optionFrame = new ReceptionistOptionFrame();
                              
                              optionFrame.setVisible(true);
                              this.dispose();
                              return;
                    }
                    
                    JOptionPane.showMessageDialog(null, "Please try Again!", "Error!",JOptionPane.ERROR_MESSAGE);
            }
            catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Try Again! . Problem in DB " + e.getMessage(), "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
            }
    }//GEN-LAST:event_btnLogInActionPerformed

