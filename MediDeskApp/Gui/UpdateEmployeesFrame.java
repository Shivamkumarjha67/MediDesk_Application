    private void loadEmpId() {
            try {
                    List<String> empIdList = EmpDao.getAllEmployeesId();
                    for(String id : empIdList) {
                            cbEmpId.addItem(id);
                    }
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error in DB. Try Again!", "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
            }
    }

    private boolean validateInput() {
            empName = txtName.getText().trim();
            empSal = txtSal.getText().trim();
            
            if(empName.isEmpty() || empSal.isEmpty())
                return true;
            
            return false;
    }
