    private void cbEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbEmpIdActionPerformed
            String empId = cbEmpId.getSelectedItem().toString();
            
            try {
                    EmpPojo epojo = EmpDao.getDetailForId(empId);
                    txtName.setText(epojo.empName);
                    txtSal.setText(String.valueOf(epojo.empSal));
                    cbDep.setSelectedItem(epojo.getEmpJob().toUpperCase());
            }
            catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Problem in DB. Try Again!", "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
            }
    }//GEN-LAST:event_cbEmpIdActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
            if(validateInput()) {
                    JOptionPane.showMessageDialog(null, "Please fill all feilds first!", "Error!", JOptionPane.ERROR_MESSAGE);
                    return;
            }
            
            try {
                    double empSalary = Double.parseDouble(empSal);
                    EmpPojo epojo = new EmpPojo();
                    
                    epojo.setEmpSal(empSalary);
                    epojo.setEmpId(cbEmpId.getSelectedItem().toString());
                    epojo.setEmpJob(cbDep.getSelectedItem().toString());
                    epojo.setEmpName(empName);
                    
                    boolean result = EmpDao.updateEmployee(epojo);
                    
                    if(result) {
                              JOptionPane.showMessageDialog(null,"Record updated successfully!","Success",JOptionPane.INFORMATION_MESSAGE);
                              if(UserProfile.getUserId().equalsIgnoreCase(epojo.getEmpId()))
                                    UserProfile.setUserName(empName);
                              return;
                    }
                    
                    JOptionPane.showMessageDialog(null,"Record not updated!","Failure!",JOptionPane.ERROR_MESSAGE);
            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
      catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null,"Please input numeric value for salary","Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

