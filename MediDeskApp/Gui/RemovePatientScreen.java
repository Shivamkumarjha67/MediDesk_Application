    private void loadAllPatIds() {
            try {
                    List<String> patIds = PatientsDao.getAllPatientsId();
                    
                    if(patIds.isEmpty()) {
                              JOptionPane.showMessageDialog(null, "No patients to delete ! ");
                              return;
                    }
                    
                    for(String id : patIds) {
                              cbPatIds.addItem(id);
                    }
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Problem in DB ! " + e);
                    e.printStackTrace();
            }
    }
