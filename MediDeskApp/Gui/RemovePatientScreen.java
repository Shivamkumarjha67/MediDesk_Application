    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
            try {
                    String patId = cbPatIds.getSelectedItem().toString();
                    boolean result = PatientsDao.deletePatient(patId);
                    
                    if(result) {
                              JOptionPane.showMessageDialog(null, "Successfully Deleted patients from DB!");
                              cbPatIds.removeItem(patId);
                              
                              if(cbPatIds.getItemCount() == 0) {
                                        ReceptionistOptionFrame fr = new ReceptionistOptionFrame();
                                        this.dispose();
                                        fr.setVisible(true);
                              }
                              return;
                    }
                    
                    JOptionPane.showMessageDialog(null, "Couldn't delete patient. Please Try again !! " );
            }
            catch(SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error in DB ! " + e);
                    e.printStackTrace();
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

