    private void btnAddEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmpActionPerformed
            if(validinputs()) {
                    JOptionPane.showMessageDialog(null, "Please Fill All the area first!!", "Error!", JOptionPane.ERROR_MESSAGE);
                    return;
            }
            
            EmpPojo aep = new EmpPojo();
            
            aep.setEmpId(EmpId);
            aep.setEmpJob(EmpJob);
            aep.setEmpName(EmpName);
            aep.setEmpSal(EmpSal);
           
            try {
                    EmpDao.AddEmployees(aep);
                    generateNewId();
            }
            catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Error in DB. Try Again!", "Error!", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnAddEmpActionPerformed

