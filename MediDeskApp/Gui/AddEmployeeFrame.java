    private boolean validinputs() {
            getInput();
            
            if(EmpId.isEmpty() || EmpName.isEmpty() || EmpJob.isEmpty())
                return true;
            
            return false;
    }

    private void getInput() {
            EmpId = txtEmpId.getText();
            EmpName = txtEmpName.getText().trim();
            EmpJob = cbJob.getSelectedItem().toString();
            try {
                    EmpSal = Double.parseDouble(txtEmpSal.getText().trim());
            }
            catch(NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Please input Salary in number format Only!", "Error!", JOptionPane.ERROR_MESSAGE);
                    return;
            }
    }

    private void generateNewId() {
             try {
                    String empId =EmpDao.getNextId();
                    txtEmpId.setText(empId);
                    txtEmpName.setText("");
                    txtEmpSal.setText("");
                    cbJob.setSelectedIndex(0);
             }
             catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Problem in generating New Id!", "Error!", JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
             }
    }
